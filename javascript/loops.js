//Loops -> Similar to java 
//For loop, Do while loop,while loop

//Printing 0-9 using for loop in js
console.log("Using for loop...");
for(let i=0;i<10;i++){
    console.log(i);
}

//Printing 0-9 using while loop
console.log("Using while loop...");
let i=0;
while(i<10){
    console.log(i);
    i++;
}

//Printing 0-9 using do while loop
console.log("USing do while loop");
var j=0;
do{
    console.log(j);
    j++;
}while(j<10);
